# გაკვეთილზე განხილული შედარების ოპერატორებიდან (>, <, ==, >=, <=, !=) თითოეულზე ჩამოწერეთ 5-5 მაგალითი

# >
print(10 > 1)
print(2 > 12)
print(2 > 1)
print(-1 > 1)
print(2 > 0)

# <
print(2 < 10)
print(1 < 1)
print(2 < 23)
print(-13 < 1)
print(2 < 12)

# ==
print(2 == 2)
print(5 == 2)
print(-10 == 10)
print(12 == 12)
print(1 == 2)

# >=
print(2 >= 2)
print(3 >= 1)
print(10 >= 2)
print(2 >= 19)
print(1 >= 18)

# <=
print(1 <= 2)
print(2 <= 20)
print(2 <= -20)
print(3 <= 10)
print(15 <= 1)

# !=
print(2 <= 2)
print(2 <= 15)
print(12 <= 12)
print(-2 <= -2)
print(12 <= 100)



