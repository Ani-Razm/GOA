// შექმენით arrow function, რომელიც იღებს ორი რიცხვს და აბრუნებს true თუ პირველი რიცხვი მეტია მეორეზე. თუ არ არის პარამეტრები, გამოიყენეთ 5 და 10 როგორც დეფოლტი.
const isGreater = (a = 5, b = 10) => a > b;

