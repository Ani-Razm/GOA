# For loop დახმარებით ეცადეთ, რომ სიტყვა დაპრინტოთ საპირისპირო მიმართულებით (შეიძლება არ გამოგივიდეთ მაგრამ სცადეთ)

word = "cat"

reversed_word = ""  # ვქმნი ცარიელ ცვლადს,სადაც შემდგომ შევინახავ შემობრუნებულ სტრინგს

for i in word: 
   reversed_word = i + reversed_word
# თავდაპირველად ცვლადში ინახება პირველი ასო
# შემდგომ პირველ ასოს მარცხნიდან ემატება მომდევნო ასოები სტრინგში 
# საბოოლოოდ სტრინგი საპირისპირო მიმართულებით იპრინტება

print(reversed_word)